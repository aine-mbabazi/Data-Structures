tuple1 = (100,200,300,400,500)
tuple2 = (600,700,800,900,1000)
# tuple length
print(len(tuple1))

# tuple concatenation
tuple3 = tuple1+tuple2
print(tuple3)

# Checking for an element in a tuple
print(tuple1[1])

# tuple repition
print(tuple1*2)

# iteration(multiplies each element in the tuple)
for a in tuple2 : print(a*2)